    private void btn_hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_hitungActionPerformed
        if(namamakanan.equals("Rendang Daging")) total=jbeli*8000;
        else if(namamakanan.equals("Rendang Ayam")) total=jbeli*7000;
        else if(namamakanan.equals("Ayam Pop")) total=jbeli*6500;
        txt_total.setText(String.valueOf(total));
    }//GEN-LAST:event_btn_hitungActionPerformed

    private void btn_bayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_bayarActionPerformed
        bayar=txt_bayar.getText();
        byr=Integer.parseInt(bayar);
        kembali=byr-total;
        txt_kembali.setText(String.valueOf(kembali));
    }//GEN-LAST:event_btn_bayarActionPerformed

