    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        jumlahbeli=txt_jumlahbeli.getText();
        jbeli=Integer.parseInt(jumlahbeli);
        namamakanan=txt_namamakanan.getText();
        tabelpenjualan.setValueAt(txt_nota.getText(),x, 0);
        tabelpenjualan.setValueAt(txt_tanggal.getText(),x, 1);
        tabelpenjualan.setValueAt(txt_jumlahbeli.getText(), x, 2);
        tabelpenjualan.setValueAt(txt_harga.getText(), x, 3);
        tabelpenjualan.setValueAt(txt_namamakanan.getText(), x, 4);
        x=x+1;
        txt_tanggal.setText("");
        txt_jumlahbeli.setText("");
        txt_nota.setText("");
        txt_harga.setText("");
        txt_namamakanan.setText("");
        txt_stok.setText("");
    }//GEN-LAST:event_btn_tambahActionPerformed

